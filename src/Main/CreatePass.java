    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(t1.getText()==null || t2.getPassword()==null || t3.getPassword()==null)
        {
            JOptionPane.showMessageDialog(null,"ENTER NECESSARY DETAILS");
        }
        else 
        {
            String p1 = t2.getText();
            String p2 = t3.getText();
            //JOptionPane.showMessageDialog(null,p1);
            //JOptionPane.showMessageDialog(null,p2);
            if(p1.equals(p2)){
                try{
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/application","root","");
                    Statement stmt = con.createStatement();
                    String q1;
                    q1 = "insert into signin values('" +t1.getText()+"','"+t3.getText()+"')";
                    //JOptionPane.showMessageDialog(null,q1);
                    stmt.executeUpdate(q1);//UPDATE queries do not return values - they return the number of rows that were altered or affected.
                    JOptionPane.showMessageDialog(null,"ACCOUNT CREATED");
                    
                    MainPage m = new MainPage();
                    m.setVisible(true);
                    this.dispose();
                }
                catch(ClassNotFoundException | SQLException e){
                    JOptionPane.showMessageDialog(this,e);
                }
            }
            else {
                JOptionPane.showMessageDialog(null,"PASSWORDS DO NOT MATCH");
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

